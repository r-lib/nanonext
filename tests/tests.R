nanotestn <- function(x) invisible(is.null(x) || {print(x); stop("the above was returned instead of NULL")})
nanotestnn <- function(x) invisible(!is.null(x) || stop("is NULL when expected to be not NULL"))
nanotestp <- function(x) invisible(is.character(capture.output(print(x))) || stop("print output of expression could not be captured as a character value"))
nanotestxp <- function(x) invisible(typeof(x) == "externalptr" || {print(x); stop("the above was returned instead of an external pointer")})
nanotesterr <- function(x, e = "") {
  x <- tryCatch(x, error = identity)
  inherits(x, "error") && grepl(e, x[["message"]], fixed = TRUE) || stop("expected error message containing '", e, "' was not generated")
  invisible(TRUE)
}
